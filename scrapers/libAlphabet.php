//php "tolowercase()" only handles the basic 26 letters
//note: some of these special characters are stored as two-character-strings, so cannot use str_split or indexing into string (ex À => ��)
function customToLowerCase($text)
{
	global $upperToLowerDict;
	
	//$ignoreChars = array(" ", "-", "_", ",", ".");
	
	foreach($upperToLowerDict as $key => $value)
	{
		$text = str_replace($key, $value, $text);
	}
	return strtolower($text);
}

